    public boolean loadData() {
        txtMaNhanVien.setText(maNV);
        jTextFieldMaNV.setText(maNV);
        String tenDangNhap = TKBUS.getTenDangNhapTheoMa(maNV);
        if (tenDangNhap.equals("")) {
            new dialog("Nhân viên này chưa có tài khoản!", dialog.ERROR_DIALOG);
            return false;
        }
        txtTenDN.setText(tenDangNhap);//
        txtMatKhauTTTK.setText(TKBUS.getMatKhauByMaNV(maNV));//lấy mật khẩu từ truy vấn từ mã nhân viên trong tài khoản lên

        BoxQuyen.removeAllItems();
        ArrayList<PhanQuyenDTO> dsq = PQBUS.getList();
        for (PhanQuyenDTO pq : dsq) {
            BoxQuyen.addItem(pq.getTenQuyen());
        }

        String maQuyen = TKBUS.getQuyenTheoMa(maNV); 
        // lấy mã quyền
        PhanQuyenDTO currentQuyen = PQBUS.getById(maQuyen); // lấy quyền
        if (currentQuyen == null) {
            return false;
        }
        for (int i = 0; i < BoxQuyen.getItemCount(); i++) {
            if ((BoxQuyen.getItemAt(i) + "").equals(currentQuyen.getTenQuyen())) {
                BoxQuyen.setSelectedIndex(i);
                break;
            }
        }
        return true;
    }
