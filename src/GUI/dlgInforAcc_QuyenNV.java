    private void btnXacNhanDoiMatKhauMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXacNhanDoiMatKhauMouseClicked
        String mk = TKBUS.getMatKhauByMaNV(maNV);
        String mkCu = txtMatKhauHT.getText().trim();
        String mkMoi = txtMatKhauMoi.getText().trim();
        String nhapLaiMkMoi = txtNhapLaiMatKhauMoi.getText().trim();

        int maxlenght = 50;
        int minLenght = 6;
        String regex = "^[a-zA-Z0-9!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>/?]*$";
        if(mkCu.equals(""))
            new dialog("Không được để trống mặt khẩu hiện tại", dialog.ERROR_DIALOG);

        else if(!mkCu.equals(mk))
            new dialog("Mật khẩu hiện tại sai", dialog.ERROR_DIALOG);
        
        else if(mkMoi.equals(mk))
            new dialog("Mật khẩu mới trùng với mật khẩu hiện tại", dialog.ERROR_DIALOG);

        else if(mkMoi.equals(""))
            new dialog("Không được để trống mặt khẩu mới", dialog.ERROR_DIALOG);

        else if(mkMoi.contains(" "))
            new dialog("Không được có khoảng cách trong mật khẩu mới", dialog.ERROR_DIALOG);

        else if(mkMoi.length() > maxlenght)
            new dialog("Mật khẩu mới không được quá 50 ký tự", dialog.ERROR_DIALOG);

        else if(mkMoi.length() < minLenght)
            new dialog("Mật khẩu mới không được ít hơn 6 ký tự", dialog.ERROR_DIALOG);

        else if(!mkMoi.matches(regex))
            new dialog("Mật khẩu mới không được có dấu", dialog.ERROR_DIALOG);

        else if(nhapLaiMkMoi.equals(""))
            new dialog("Mật khẩu nhập lại không được để trống", dialog.ERROR_DIALOG);

        else if(!nhapLaiMkMoi.equals(mkMoi))
            new dialog("Mật khẩu mới nhập lại không khớp", dialog.ERROR_DIALOG);

        else{
            boolean check = TKBUS.doiMatKhau(mkMoi, mkCu, maNV);
            if(check){
                txtMatKhauHT.setText("");
                txtMatKhauMoi.setText("");
                txtNhapLaiMatKhauMoi.setText("");
            }

        }
    }//GEN-LAST:event_btnXacNhanDoiMatKhauMouseClicked


